class User {
  constructor(
    name,
    userName,
    email,
    mobileNo,
    role,
    active,
    password,
    createdAt,
  ) {
    (this.name = name),
      (this.userName = userName),
      (this.email = email),
      (this.mobileNo = mobileNo),
      (this.role = role),
      (this.password = password),
      (this.active = active),
      (this.createdAt = createdAt);
  }
}
